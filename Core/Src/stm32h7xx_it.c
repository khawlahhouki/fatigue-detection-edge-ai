void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    BaseType_t xHigherPriorityTaskWoken = pdFALSE;

    if(GPIO_Pin == INT_PPG_Pin){
    	 // CAPTURE IMMEDIATE DU TEMPS ICI
    	ppg_isr_timestamp = __HAL_TIM_GET_COUNTER(&htim2);
        vTaskNotifyGiveFromISR(vTaskPPGAcqHandle, &xHigherPriorityTaskWoken);
    }
    if(GPIO_Pin == DRDY_EMG_Pin){
    	/* CAPTURE IMMEDIATE DU TEMPS ICI */
    	 emg_isr_timestamp = __HAL_TIM_GET_COUNTER(&htim2);
        vTaskNotifyGiveFromISR(vTaskEMGAcqHandle, &xHigherPriorityTaskWoken);
    }
    if(GPIO_Pin == INT1_IMU_Pin){
    	/* CAPTURE IMMEDIATE DU TEMPS ICI */
    	Imu_isr_timestamp = __HAL_TIM_GET_COUNTER(&htim2);
        vTaskNotifyGiveFromISR(vTaskIMUAcqHandle, &xHigherPriorityTaskWoken);
    }


    portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
}

void HAL_SPI_RxCpltCallback(SPI_HandleTypeDef *hspi)
{
    BaseType_t xHigherPriorityTaskWoken = pdFALSE;

    if(hspi->Instance == SPI2)
    {
        HAL_GPIO_WritePin(CS_PPG_GPIO_Port, CS_PPG_Pin, GPIO_PIN_SET);
        xSemaphoreGiveFromISR(sem_dma_ppg, &xHigherPriorityTaskWoken);
    }

    portYIELD_FROM_ISR(xHigherPriorityTaskWoken);
}
